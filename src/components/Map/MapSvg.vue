<script lang="ts">
import MapPopover from './MapPopover.vue';
import { defineComponent, onMounted, PropType, ref } from 'vue';
import IBuilding from '@/interfaces/buildings/IBuilding';
import IDivision from '@/interfaces/divisions/IDivision';

export default defineComponent({
  name: 'MapSvg',
  components: {
    MapPopover,
  },
  props: {
    buildings: {
      type: Array as PropType<Array<IBuilding>>,
      required: true,
    },
    divisions: {
      type: Array as PropType<Array<IDivision>>,
      required: true,
    },
  },
  async setup(props) {
    let buildingId = ref('');
    let position = ref();
    let building = ref();

    const decor = ref<HTMLDivElement>();
    const buildingsRef = ref<HTMLDivElement>();
    const filterDivisions = (buildingId: string) => {
      return props.divisions.filter((item: IDivision) => {
        return item.building_id === parseInt(buildingId.substr(2, 2));
      });
    };

    const treeJump = (item: any) => {
      item.classList.add('jump');
      setTimeout(function () {
        item.classList.remove('jump');
      }, 1000);
    };

    const hoverBuilding = (item: any) => {
      // building_element.value = item;
      buildingId.value = '';
      item.classList.add('flicker');
      building.value = props.buildings.find((b) => {
        return parseInt(b.id) === parseInt(item.id.substr(2, 2));
      });

      setTimeout(function () {
        buildingId.value = item.id;
        item.classList.remove('flicker');
      }, 500);
    };

    onMounted(() => {
      if (!decor.value || !buildingsRef.value) return;
      decor.value.childNodes.forEach((n) =>
        n.addEventListener('mouseover', function () {
          treeJump(n);
        })
      );

      buildingsRef.value.childNodes.forEach((item: any) => {
        item.addEventListener('click', () => {
          let parentPos = document.getElementById('map-svg')!.getBoundingClientRect(),
            childrenPos = item.getBoundingClientRect(),
            relativePos = { top: 0, right: 0, bottom: 0, left: 0 };

          relativePos.top = childrenPos.top - parentPos.top;
          relativePos.right = childrenPos.right - parentPos.right;
          relativePos.bottom = childrenPos.bottom - parentPos.bottom;
          relativePos.left = childrenPos.left - parentPos.left;
          if (position && position.value !== null) {
            position.value = relativePos;
          }

          hoverBuilding(item);
        });
      });
    });

    return {
      building,
      buildingId,
      position,
      decor,
      buildingsRef,
      filterDivisions,
      treeJump,
      hoverBuilding,
    };
  },
});

